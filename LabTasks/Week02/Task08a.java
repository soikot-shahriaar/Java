// Write a program that checks if a number is even or odd and prints the result.

class Task08a {

    public static void main(String[] args) {

        int number = 10;
        
        if (number % 2 == 0) {
            System.out.println(number + " is even");
        } else {
            System.out.println(number + " is odd");
        }
    }
}
