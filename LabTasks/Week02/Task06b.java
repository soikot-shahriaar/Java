// Create a program that uses increment and decrement operators on a variable and prints the result each time.

class Task06b {

    public static void main(String[] args) {

        int x = 999;

        System.out.println("x++ = " + (x++));
        System.out.println("x++ = " + (++x));
        System.out.println("x-- = " + (x--));
        System.out.println("x-- = " + (--x));
    }
}
