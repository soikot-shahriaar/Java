/* 
do {
while (condition) {
    // Code block to be executed
}
 */

public class S_DoWhileLoop {

    public static void main(String[] args) {
        
        int i = 1; // Initialization

        do {
            System.out.println("Soikot Shahriar 00" + i); // Code to be executed
            i++; // Increment
        } while (i <= 7); // Condition
    }
}
